    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        Usuario usuario = new Usuario();
        UsuarioDAO usuariodao = new UsuarioDAO();
        boolean verifica = true;
        
        for (Usuario u : usuariodao.findAll()) {
            if (u.getLogin().equals(jTFLogin.getText())) {
                if (jTFCodigo.getText().equals("")) {
                    verifica = false;
                    break;
                }
            }
        }
        if (!verifica) {
            JOptionPane.showMessageDialog(this, " Login j√° existente!");
        }
        if (verifica) {
            if (jPFSenha.getText().length() < 6) {
                JOptionPane.showMessageDialog(this, " A senha deve conter no minimo 6 caracteres!");
            } else {
                usuario.setNome(jTFNome.getText());
                usuario.setLogin(jTFLogin.getText());
                usuario.setSenha(criptografar(jPFSenha.getText()));

                if (jTFTipo.getText().equals("Administrador")) {
                    usuario.setTipo(1);
                } else {
                    usuario.setTipo(2);
                }
                if (jTFCodigo.getText().equals("")) {
                    UsuarioDAO.getInstance().persist(usuario);
                } else {
                    usuario.setCodigo(Integer.parseInt(jTFCodigo.getText()));
                    UsuarioDAO.getInstance().merge(usuario);
                }
                limpar();
                JOptionPane.showMessageDialog(this, " Salvo com sucesso!");
            }
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jBSalvarActionPerformed

    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        Usuario usuario = new Usuario();
        int confirma = 0;
        String nome = (JOptionPane.showInputDialog(this, "Informe o nome de Usu√°rio:"));
        List<Usuario> lista = UsuarioDAO.getInstance().findAll();
        for (Usuario u : lista) {
            if (u.getLogin().equals(nome)) {
                usuario = u;
                confirma = 1;
                break;
            }
        }
        if (confirma == 0) {
            JOptionPane.showMessageDialog(null, "Usu√°rio n√£o encontrado!");
        }
        jTFCodigo.setText(String.valueOf(usuario.getCodigo()));
        jTFNome.setText(usuario.getNome());
        jTFLogin.setText(usuario.getLogin());
        jTFTipo.setText(String.valueOf(usuario.getTipo()));
        //jTFSenha.setText(usuario.getSenha());
        jBExcluir.setEnabled(true);     // TODO add your handling code here:
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        Usuario usuario = new Usuario();
        int cod;
        cod = Integer.parseInt(JOptionPane.showInputDialog(this, "Confirme o Codigo:"));
        List<Usuario> lista = UsuarioDAO.getInstance().findAll();
        for (Usuario c : lista) {
            if (c.getCodigo() == cod) {
                usuario = c;
                break;
            }
        }
        UsuarioDAO.getInstance().removeById(cod);
        limpar();
        jBExcluir.setEnabled(false);     // TODO add your handling code here:
    }//GEN-LAST:event_jBExcluirActionPerformed

